        // TODO add your handling code here:
        int selectedRowIndex = jTable3.getSelectedRow();

        if (selectedRowIndex == -1) {
            jOptionPane1.showMessageDialog(Admin_function.this, "Please select a row to update.");
            return;
        }

        // Get the data from the selected row
        String id = jTable3.getValueAt(selectedRowIndex, 0).toString();
        String newName = txtName4.getText();
        String newContact = txtContact4.getText();
        String newPassword = txtPassword4.getText();

        // Read the existing content of the file
        ArrayList<String> lines = new ArrayList<>();
