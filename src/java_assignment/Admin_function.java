    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = jTable2.getSelectedRow();

        if (selectedRowIndex == -1) {
            jOptionPane1.showMessageDialog(Admin_function.this, "Please select a row to update.");
            return;
        }

        // Get the data from the selected row
        String id = jTable2.getValueAt(selectedRowIndex, 0).toString();
        String newName = txtName3.getText();
        String newContact = txtContact3.getText();
        String newPassword = txtPassword3.getText();

        // Read the existing content of the file
        ArrayList<String> lines = new ArrayList<>();
        try (BufferedReader br = new BufferedReader(new FileReader("CustomerData.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                lines.add(line);
            }
        } catch (IOException e) {
            e.printStackTrace();
            jOptionPane1.showMessageDialog(Admin_function.this, "Error reading from file: " + e.getMessage(), "Error", jOptionPane1.ERROR_MESSAGE);
            return;
        }

        // Update the selected row's data
        String updatedRow = id + "," + newName + "," + newContact + "," + newPassword;
        lines.set(selectedRowIndex, updatedRow);

        // Write the modified content back to the file
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("CustomerData.txt"))) {
            for (String line : lines) {
                bw.write(line);
                bw.newLine();
            }
            jOptionPane1.showMessageDialog(Admin_function.this, "Customer information updated successfully!");
        } catch (IOException e) {
            e.printStackTrace();
            jOptionPane1.showMessageDialog(Admin_function.this, "Error writing to file: " + e.getMessage(), "Error", jOptionPane1.ERROR_MESSAGE);
        }

        // Clear text fields after updating
        clearTextFields();
        // Refresh the table with updated data
        displayCustomerDataInTable();
    }//GEN-LAST:event_jButton7ActionPerformed

