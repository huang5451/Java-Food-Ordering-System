    private void writeToTextFile(String id, String Name,String Contact, String Password, double Credit){
        try(BufferedReader br = new BufferedReader(new FileReader("CustomerData.txt"))){
            String line;
            while((line = br.readLine()) != null){
            String[] parts = line.split(",\\s*");
            String existingID = parts[0].trim();
            
            //check for duplicate customer ID 
            if(existingID.equals(id)){
                jOptionPane1.showMessageDialog(Admin_function.this,"ID is already registered!");
                return;
            }
        }
        }catch (IOException e){
            // Handle IOException, e.g., display an error message
            e.printStackTrace();
        }
        
        try(BufferedWriter bw = new BufferedWriter(new FileWriter("CustomerData.txt",true))){
            //Append the new registration to the file
            bw.write(id +  "," + Name + "," + Contact + "," + Password + "," + Credit + "\n");
            
            jOptionPane1.showMessageDialog(Admin_function.this, "Register Customer Succesfully!");
        } catch (IOException e) {
            jOptionPane1.showMessageDialog(Admin_function.this,"Register Failed: " + e.getMessage(), "Error",jOptionPane1.ERROR_MESSAGE);
        }
        clearTextFields();
    }
    
