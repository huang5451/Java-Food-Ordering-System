    private void writeToTextFile(int id, String rating, String feedback, String item) {
    
    try (BufferedWriter bw = new BufferedWriter(new FileWriter("ReviewData.txt", true))) {

        bw.write(id+","+feedback+","+rating+","+item+"\n");

        jOptionPane1.showMessageDialog(Customer_Review.this, "Review written to file successfully!");
    } catch (IOException e) {
        jOptionPane1.showMessageDialog(Customer_Review.this, "Error writing to file: " + e.getMessage(), "Error", jOptionPane1.ERROR_MESSAGE);
    }
}
    

