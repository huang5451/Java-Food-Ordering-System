    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        loadLastId();
       
        int id = ++lastId;
        String selectedRating = (String) jComboBox1.getSelectedItem();
        String feedback = jTextField1.getText();
        String selectedFood = (String) jComboBox2.getSelectedItem();
        
        if (feedback.isEmpty()) {
        jOptionPane1.showMessageDialog(Customer_Review.this, "Please give feedback for the food.", "Empty Feedback", jOptionPane1.WARNING_MESSAGE);
        return;
    }
        // Set values in the RatingReview object
        rr.setId(id);
        rr.setRating(selectedRating);
        rr.setFeedback(feedback);
        rr.setItem(selectedFood);
        
        // Display review
        jOptionPane1.showMessageDialog(Customer_Review.this, rr.toString());
        // Write file
        writeToTextFile(id,feedback, selectedRating, selectedFood);
        
    }//GEN-LAST:event_jButton1ActionPerformed
    private void loadLastId() {
        try (BufferedReader br = new BufferedReader(new FileReader("ReviewData.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length > 0) {
                    int id = Integer.parseInt(parts[0]);
                    if (id > lastId) {
                        lastId = id;
                    }
                }
            }
        } catch (IOException | NumberFormatException e) {
            // Handle the exception (e.g., log or show an error message)
            e.printStackTrace();
        }
    }
